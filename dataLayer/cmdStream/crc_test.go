package cmdStream

import (
	"hash/crc32"
	"log"
	"testing"
)

func TestCrc32(t *testing.T) {
	// 0 39 118 52 46 48 95 49 46 48 46 60 46 48 95 122 104 45 72 65 78 83 95 51 49 98 102 60 200 53 54 97 100 60 198 52 101 60 197 13 10 89 241 6 20
	var data = []byte{0, 39, 118, 52, 46, 48, 95, 49, 46, 48, 46, 60, 46, 48, 95, 122, 104, 45, 72, 65, 78, 83, 95, 51, 49, 98, 102, 60, 200, 53, 54, 97, 100, 60, 198, 52, 101, 60, 197, 13, 10}
	hash := crc32.NewIEEE()
	_, _ = hash.Write(data)

	log.Printf("0x%x\n", hash.Sum32())
}
